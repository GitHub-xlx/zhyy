package com.zhyy.entity;

public class GainAndLoss
{

	private String invid;
	private String drugcode;
	private String inventoryresults;
	private String pharmacycode;
	private String inventorytime;
	private String commoname;

	public String getInvid()
	{
		return invid;
	}

	public void setInvid(String invid)
	{
		this.invid = invid;
	}

	public String getDrugcode()
	{
		return drugcode;
	}

	public void setDrugcode(String drugcode)
	{
		this.drugcode = drugcode;
	}

	public String getInventoryresults()
	{
		return inventoryresults;
	}

	public void setInventoryresults(String inventoryresults)
	{
		this.inventoryresults = inventoryresults;
	}

	public String getPharmacycode()
	{
		return pharmacycode;
	}

	public void setPharmacycode(String pharmacycode)
	{
		this.pharmacycode = pharmacycode;
	}

	public String getInventorytime()
	{
		return inventorytime;
	}

	public void setInventorytime(String inventorytime)
	{
		this.inventorytime = inventorytime;
	}

	public String getCommoname()
	{
		return commoname;
	}

	public void setCommoname(String commoname)
	{
		this.commoname = commoname;
	}
}
